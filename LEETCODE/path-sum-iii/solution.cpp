  struct TreeNode {
      int val;
      TreeNode *left;
      TreeNode *right;
      TreeNode(int x) : val(x), left(NULL), right(NULL) {}
  };
 
class Solution {
public:
    map<int,int> partialSum;
    int pathSum(TreeNode* root, int sum) {
        if(!root)
            return 0;
        partialSum[0] = 1;
        return countSums(root, sum, 0);
    }
    
    int countSums(TreeNode* root, int target, int cur_sum){
        if(!root)
            return 0;

        cur_sum += root->val;
        int res = partialSum[cur_sum-target];
        partialSum[cur_sum]++;
        res = res + countSums(root->left, target, cur_sum) + countSums(root->right, target, cur_sum);
        partialSum[cur_sum]--;
        return res;
    }
};

