class Stack {
public:
    // Push element x onto stack.
	queue<int> L;
	queue<int> R;
    void push(int x) {
        if(L.empty())
            R.push(x);
        else
            L.push(x);
    }
    
    void pop_until_last(queue<int>& L, queue<int>& R){
        while(L.size() > 1){
            int e = L.front(); L.pop();
            R.push(e);
        }
    }

    // Removes the element on top of the stack.
    void pop() {
        if(L.empty()){
            pop_until_last(R,L);
            R.pop();
        }
        else{
            pop_until_last(L,R);
            L.pop();
        }
    }

    // Get the top element.
    int top() {
        if(L.empty())
            return R.back();
        else
            return L.back();
    }

    // Return whether the stack is empty.
    bool empty() {
        return L.empty() && R.empty();
    }
};

