int number_needed(string a, string b) {
   vector<int> freq_a(26,0);
   vector<int> freq_b(26,0);
   for(int i=0; i<a.size(); ++i)
       freq_a[a[i]-'a']++;
   for(int i=0; i<b.size(); ++i)
       freq_b[b[i]-'a']++;
   int cnt=0;
   for(int i=0; i<26; ++i)
       cnt += abs(freq_a[i] - freq_b[i]);
   return cnt;
}

