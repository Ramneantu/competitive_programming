bool is_balanced(string expression) {
    stack<char> br;
    for(int i=0; i<expression.size(); ++i){
        char c = expression[i];
        if(c == '(' || c == '[' || c == '{')
            br.push(c);
        else if(c == ')'){
            if(br.empty() || br.top() != '(')
                return false;
            br.pop();
        }
        else if(c == ']'){
            if(br.empty() || br.top() != '[')
                return false;
            br.pop();
        }
        else if(c == '}'){
            if(br.empty() || br.top() != '{')
                return false;
            br.pop();
        }
    }
    if(!br.empty())
        return false;
    return true;
}

int main(){
    int t;
    cin >> t;
    for(int a0 = 0; a0 < t; a0++){
        string expression;
        cin >> expression;
        bool answer = is_balanced(expression);
        if(answer)
            cout << "YES\n";
        else cout << "NO\n";
    }
    return 0;
}
